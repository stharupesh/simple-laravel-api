<?php

namespace App\Http\Resources\Make;

use Illuminate\Http\Resources\Json\JsonResource;

class Make extends JsonResource
{
    /**
     * Transform the resource into an array.
     *
     * @param $request
     * @return array
     */
    public function toArray($request): array
    {
        return [
            'id' => $this->id,
            'name' => $this->name
        ];
    }
}
